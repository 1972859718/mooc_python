#画叠边形
import turtle as t
t.pensize(5)
t.penup()
t.bk(200)
t.right(90)
t.fd(200)
t.left(90)
t.pendown()
t.fd(300)
t.left(80)
t.fd(300)
t.left(80)
t.fd(300)
t.left(80)
t.fd(300)
t.left(80)
t.fd(300)
t.left(80)
t.fd(300)
t.left(80)
t.fd(300)
t.left(80)
t.fd(300)
t.left(80)
t.fd(300)
t.left(80)
t.done()
